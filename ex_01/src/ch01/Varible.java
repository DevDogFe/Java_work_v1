package ch01;

public class Varible {

	// 코드 시작점
	public static void main(String[] args) {
		
		int age = 1;
		int count = 100;
		
		System.out.println(age);
		System.out.println("================");
		System.out.println(count);
		
		age = 500;
		count = 300;
		
		System.out.println(age);
		System.out.println("================");
		System.out.println(count);

		int myAge = 800;
		System.out.println(myAge);
		System.out.println(9000);
		
	} //end of main

} //end of class
