package ch01;

public class DataType4 {

	// 코드 시작점
	public static void main(String[] args) {

		boolean isMarried = false;
		System.out.println(isMarried);

		isMarried = true;
		System.out.println(isMarried);
		
		int a = 1;
		System.out.println(a == 0);
		
		boolean aa = a == 0;
		boolean aaa = a == 1;
		System.out.println(aa);
		System.out.println(aaa);
	}// end of main

}// end of class
